      <b>3.2.1 - EXTENDIDO MANUAL, SE HACE EN DOS FORMAS:</b> <br>


    </h5>
    <h5>
      3.2.1.1- EXTENDIDO EN ZIG – ZAG

    </h5>
  </li>
  <li class="collection-item">
    En este extendido se utilizan unas barras metálicas planas colocadas al principio y final del extendido respectivamente.

    El tejido se lleva manualmente hasta el otro extremo del tendido colocando sobre éste una barra metálica sobre estas se pliega el tejido y se retrocede estirando una nueva capa hasta la barra inicial , donde se realiza un nuevo plegado usando una varilla similar a la anterior. De esta forma se va repitiendo el ciclo, sacando en cada vuelta la varilla correspondiente al plegado anterior y realizando uno nuevo.

  </li>


  <li class="collection-header cyan-text text-darken-2">

    <h5>
     3.2.1.2- EXTENDIDO CON CORTE EN CADA CAPA

   </h5> <br>
 </li>
 <li class="collection-item">

  Básicamente constituyó el método usado desde los inicios de la confección . Consistía en llevar el tejido hasta una marca efectuada sobre la mesa , donde finalizaba la marcada y en dicho punto, se sujetaba la tela por medio de una barra metálica plana o pesas. Los operarios retrocedían en vacío analizando la hoja extendida, luego se procedía a cortar . Esta operación se repetía tantas veces cuando fuere necesario. <br>

  Esta forma de operación sigue vigente en muchas empresas o talleres; no obstante en la gran mayoría de ellos efectúan el extendido manual pero auxiliandosen de unos aparatos diseñados para tal operación; como, carros extendedores, cortadoras de extremos. <br>

</li>

<li class="collection-header cyan-text text-darken-2">

  <h5>

    3.2.2- EXTENDIDO AUTOMÁTICO CON MÁQUINA EXTENDEDORA:


  </h5> <br>
</li>
<li class="collection-item">


  Consiste en colocar un rollo de tela sobre un soporte móvil, que se desliza a lo largo del extendido, el cual durante su recorrido se encarga de ir dejando el tejido posado sobre la mesa de corte <br>

  Con este sistema se consiguen dos objetivos fundamentales: rapidez en los operarios para efectuar la operación y mayor aprovechamiento del tejido. <br>
</li>

<li class="collection-header cyan-text text-darken-2">

  <h5>
   <b>3.2.3 - SISTEMAS DE EXTENDIDO</b> <br>

   3.2.3.1- EXTENDIDO CARA ARRIBA

 </h5> <br>
</li>
<li class="collection-item">

  El extendido comienza en el extremo inicial de la marcada, donde se coloca el rollo, se deposita el tejido sobre la mesa hasta llegar al extremo final donde se efectúa el corte al ancho del tejido. Luego se devuelve al vacío hasta el inicio del tejido y se repite la operación cuantas veces sea necesario. <br>

  Es el extendido típico del tejido estampado de cuadros o de rayas. <br>


</li>






<li class="collection-header cyan-text text-darken-2">

  <h5>


   3.2.3.2 - EXTENDIDO CARA A CARA ZIGZAG

 </h5> <br>
</li>
<li class="collection-item">

  Se inicia en cualquiera de los extremos de la marcada. El tejido se deposita en el extremo elegido y el operario (máquina) se desliza hacia el otro extremo, dejando el tejido al mismo tiempo. Al llegar al extremo opuesto, éste es doblado y el operario (carro extendedor) retrocede extendiendo al mismo tiempo; y así sucesivamente. Este sistema es el mas rápido y se utiliza para tejido de forro de bolsillo..... como se llama el tejido? <br>


</li>

<li class="collection-header cyan-text text-darken-2">

  <h5>


   3.2.3.3- EXTENDIDO CARA A CARA GIRANDO


 </h5> <br>
</li>
<li class="collection-item">

  Se comienza al igual que en los casos del extendido a una cara, en el extremo inicial de la marcada. El operario (carro extendedor) va extendiendo hasta el extremo opuesto donde es cortado el tejido, procediendose a girar el tejido o el portarrollos (máquina transportadora) antes de volver al principio de la marcada, para empezar a extender la siguiente capa. Por tanto el tejido en segunda capa irá con su cara hacia abajo y en la misma dirección del hilo que en la primera. <br>

  Este sistema se utiliza en tejidos con pelo tales como: Terciopelo, cordoroy, pana, piel durazno, etc. Además se utiliza en tejidos para la ropa interior, como licra, naylon, (tejidos lisos, delicados y difíciles de manipular). <br>


</li>



<li class="collection-header cyan-text text-darken-2">

  <h5>


   3.2.3.4- EXTENDIDO EN ESCALAS

 </h5> <br>
</li>
<li class="collection-item">

  Es una variable entre los extendidos cara arriba y cara a cara girando, donde si bien el círculo de trabajo es idéntico, las longitudes de las capas son diferentes. Esto es debido a que se intentó extender dentro de la misma marcada tallas con cantidades diferentes. <br>

  Este tipo de extendido es empleado por confeccionistas con gran variedad de modelos y pocas cantidades por tallas, puesto que de esta manera se puede completar un pedido con una sola marcada, ahorrándose todos los tiempos de preparación de marcada y cortes que se necesitarían si se extendieran todas las tallas individuales. Al mismo tiempo se consigue mayor velocidad en el extendido, trazo y corte y por lo tanto un incremento en el rendimiento de cada mesa de corte. <br>

</li>


<li class="collection-header cyan-text text-darken-2">

  <h5>


   3.2.3.5- EXTENDIDO EN FORMA TUBULAR

 </h5> <br>
</li>
<li class="collection-item">

  El método de trabajo es idéntico a los extendidos cara arriba y Zigzag , las diferencias con respecto a ellos radica en la forma de colocación del género y a los dispositivos especiales que llevan las máquinas extendedoras para alimentar y depositar las hojas dobles del tejido tubular. <br>


</li>

<li class="collection-header cyan-text text-darken-2">

  <h5>


   3.2.3.6- EXTENDIDO DE TEJIDO A LOMO


 </h5> <br>
</li>
<li class="collection-item">

  Fue el sistema con el que se inició la confección, fue la única forma de uso que hubo en sastrería y similares, es en la actualidad una incidencia menor y progresivamente descendiente. No obstante aún tiene la suficiente importancia para que pueda ser usada una máquina con los dispositivos necesarios, a fín de que las capas de tejido doblado queden perfectamente extendidos. <br>

  El tipo de extendido de tejido a lomo es siempre en Zigzag o sus variantes en escalón <br>


</li>

