            Sumatoria de SP <br>

            <b>A</b> = ------------------------------- <br>

            Sumatoria de RM <br>

            <b>A</b> = al aprovechamiento del tejido <br>

            <b>Sumatoria SP</b> = Sumatoria superficie de los patrones <br>

            <b>Sumatoria RM</b> = Sumatoria del rectángulo de la marcada <br>

