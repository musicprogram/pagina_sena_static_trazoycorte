            El marcado o dibujado del perfil de los patrones según las características dimensionales del tejido a emplear, puede realizarse directamente sobre el tejido ( tejidos a cuadros o rayas) o sobre papel. En cualquier caso es conveniente efectuar un estudio previo de colocación de los patrones a escala reducida ( normalmente a escala 1/5).  <br>

            De la mejor o peor forma de situarlos obtendremos un consumo menor o mayor de materia prima textil que incidirá de una manera directa en el costo y precio del artículo. Por este hecho el trazo ( marcado) es un trabajo que requiere un cuidado especial porque de ello depende el aprovechamiento del tejido. <br>

