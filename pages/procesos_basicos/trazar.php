    <h5>
      3.1.1 - SISTEMAS PARA TRAZAR
    </h5>
  </li>

  <li class="collection-item">
    <div class="card">
      <div class="card-content">
        <b>a - SISTEMA MANUAL</b> <br>

        Dibujo a mano del perfil de los patrones sobre una superficie plana ya sea sobre papel o tela. Si se ha de repetir la marcada y esta se hace sobre papel, se puede hacer copias colocando varios papeles superpuestos e intercalando papel carbón entre ellos. Una vez efectuado el corte la marcada queda destruída. <br>

        <b>b - SISTEMA PERFORADO</b> <br>

        Se efectúa el trazo ( marcado ) sobre papel; una vez dibujado se procede a perforar el contorno de cada una de las piezas o figuras con un pin o punzón. Para reproducir esta marcada sobre el tejido basta colocarlo sobre el mismo y espolvorearlo con polvos tipo negro humo o talco, pasando estos tejidos y quedando adheridos a él; la misma marcada tiene una larga duración. <br>

        <b>c - SISTEMA POR PULVERIZACIÓN</b> <br>

        En este sistema la moldería debe estar completa ( ejemplo : los moldes marcados 2 x tallas y marcados izquierdo derecho). Se colocan estos sobre el tejido o el papel como armando un rompecabezas. Terminado el trazo se procede a espolvorear, ( polvos tipo negro humo o talco ) en los espacios vacíos , quedando adheridos al material y marcada la silueta de los moldes. <br>

        Se completa la marcada colocando un adhesivo (cinta pegante) en el cual se coloca la información deseada (talla, lote, etc.) <br>

        <b>d - SISTEMA FOTOGRÁFICO</b>

        Se extiende el papel fotográfico sensibilizado sobre la mesa, se depositan los patrones sobre él, siguiendo la posición del estudio previo. Se pasa seguidamente un carro con unos potentes focos de luz ultravioletas que actúan sobre las partes del papel que está al descubierto, no así en las cubiertas por el patrón, luego se fija esta imagen escalando el papel en atmósfera amoniaca. El perfil de los patrones queda marcado con extrema nitidez. <br>

        <b>- SISTEMA DE MINIATURIZACIÓN</b> <br>
        Proceso de reducir moldes reales a diferentes escala de 1/5 , 1/3, 1/8, etc… <br>

        <b>La miniaturización permite:</b> <br>

        Reducir costos <br>

        Conocer consumo o promedio <br>

        Largo del trazo <br>

        Programar el trazador <br>

        Longitud del rollo <br>

        Estudio previo de la marcada <br>
