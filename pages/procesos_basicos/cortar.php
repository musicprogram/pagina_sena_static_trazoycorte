  <li class="collection-header cyan-text text-darken-2"><h5>3.3.2.4- CORTE POR PRESIÓN ( TROQUEL)
  </h5></li>
  <li class="collection-item">

    <div class="card">
      <div class="card-content">

      <div class="cyan-text darken-3">
        Las prensas de presión pueden ser neumáticas o hidráulicas, según el esfuerzo por realizar y su forma de trabajo. <br>

        Para el trazo se utilizan moldes cortantes , los cuales se colocan en la prensa o troquel según la proporsión de talas a trazar. <br>

        El extendido se realiza igual a la técnica convencional , teniendo en cuenta que el largo y el ancho del tejido no superen el tamaño de la base del troquel. <br>

        El plato superior puede tener dos movimientos , sempre el de ascenso - descenso (efectúa el troquelado). <br>

        En algunas prendas hay desplazamiento rotatorio del plato superior para facilitar la colocación de las telas y el troquel. <br>

        Cuando el troquel de la pieza tiene unas dimensiones reducidas conviene que este tenga unos elementos de expulsión de las telas que quedan embutidas en su interior. <br>

        Cuando éste es de amplias dimensiones la expulsión se hace a mano. <br>

        <b>La técnica de corte por presión a troquel se utiliza :</b> <br>

        <b>1 -</b> en tejidos difíciles de manipular como los tejidos sintéticos ( se funden). <br>

        <b>2 -</b> tejidos a los cuales se les hace un proceso especial ( entretela fusionables ). <br>

        Estos tejidos pueden ocasionar problemas cuando son cortados con otro tipo de cortadoras ( verticales, circulares y Sin - fin). <br>

