  <li class="collection-header cyan-text text-darken-2"><h5>3.3.2.3- CORTE CON CUCHILLA DE CINTA O SIN-FIN</h5></li>
  <li class="collection-item">
    <div class="card">
      <div class="card-content">
          
          <div class="cyan-text darken-3">
            Esta máquina permanece estática, lo que se mueve es el material para cortar, empujado por la mano del operario/a. <br>

        El elemento cortante es un fleje afilado de un ancho aproximado de un (1) centímetro y espesor inferior a ½ mm, empalmando en forma de cinta sin - fin, que se mueve enter tres volantes ( según el esquema siguiente): <br>

        Los volantes 2 y 3 son fijos y el 1 es móvil para permitir la introducción de la cinta y poder tensionar la misma. <br>

        A y B son dos elementos guiadores situados en la parte superior e inferior respectivamente del plano de trabajo. <br>

        <b>Con esta disposición mecánica de corte se consigue : </b><br>

        Una mayor velocidad del elemento cortante. <br>

        Un movimiento continuo del mismo <br>

        Una perfecta perpendicularidad del elemento cortante en relación con el plano de trabajo. <br>

        Una mayor seguridad y facilidad de corte al ser la máquina fija y moverse el tejido. <br>

        En algunos modelos , próximo al guiador A o entre el volante 1 y 2 se sitúan dos muelas ( esmeriles ) que mediante presión manual se aproximan a la cinta para su afilado durante la marcha de la misma. <br>

        Para evitar el desplazamiento de las telas ( deslizamiento ) al ser movidas se sujetan estas mediante pinzas. <br>

        El corte con esta máquina es de gran precisión el cual puede aumentarse reduciendo el espesor a cortar (número de capas).

