  <li class="collection-header cyan-text text-darken-2"><h5>3.3.2.2- CORTE CON CUCHILLA VERTICAL</h5></li>
  <li class="collection-item">

   <div class="cyan-text darken-3">
      La máquina utilizada está formada por un motor eléctrico que transmite el movimiento a una cuchilla mediante ruedas dentadas (piñones) y un mecanismo de bielas. Tiene un movimiento alternativo y perpendicular a la superficie de la mesa. <br>

    El elemento cortante puede ser de filo continuo o dentado según el tipo de tejido a cortar. El afilado se hace mediante un mecanismo situado al frente o al lado de la cuchilla. Está compuesto por dos cintas SIN-FIN cruzadas de abrasivo; que, además de tener rotación las propias cintas, el conjunto del mecanismo se desplaza en sentido alternativo a todo el largo de la cuchilla estando esta en funcionamiento. <br>

    Cómo es lógico, este afilado debe efectuarse con la máquina fuera del colchón del tejido. <br>

    La máquina está diseñada para trabajo pesado, piezas grandes y tejidos rígidos.
