  -2.8.1 - SISTEMAS DE ALMACENAJE <br>
  <ul>
    <li class="cyan-text darken-3">
      <div class="card-content">

         -2.8.1.1 – ALMACENAJE SOBRE PLATAFORMA <br>

         Consiste en amontonar telas hasta cierta altura, es el sistema más económico que existe, pues requiere de menor espacio que los otros.

         Desventajas: Deformaciones internas de los tejidos, por soportar el peso de los de encima. Se hace dificil localizar determinados rollos.
       <br>
        -2.8.1.2 – ALMACENAJE SOBRE ESTANTERÍA <br>

        Método más económico para el comerciante que compra un número limitado de rollos. Se eliminan las desventajas del sistema sobre plataforma, puesto que los rollos no causan presión sobre las piezas de tejidos, especialmente en los tejidos de punto. También se pueden almacenar tejidos de punto, que es necesario tener reposados 48 horas antes de extender y en la forma adecuada ( plegados)
      <br>
        -2.8.1.3 – ALMACENAJE SOBRE PORTARROLLOS MÚLTIPLES O UNO A UNO <br>

        Con este sistema el objetivo es agilizar el proceso de extendido sin necesidad de bajar los rollos de las estanterías, es decir, el extendido se hace directamente. Se sostiene el rollo de tela pasando una barra o mandril a través del tubo o agujero.

      <br>

        -2.8.1.4 – ALMACENAJE EN BÓVEDA O PÁNELES <br>

        Este sistema nos permite almacenar tejidos que son muy delicados (sedas, lycras), que al ser almacenados en los anteriores sistemas, pueden presentar deformaciones internas. También da la facilidad de almacenar tejidos en reposo como son los tejidos de punto.
