




<ul class="collection with-header">
  <li class="collection-header cyan-text text-darken-2 right-align"><h5>CLASIFICACIÓN DE LAS TELAS</h5></li>
 
  <li class="collection-item">
   -2.2.1 -Según su uso final se clasifican en <br>
    <b>- Telas pesadas:</b> lonas para carpas, driles para jean etc.  <br>
    <b>- Telas de mediano peso:</b> Driles, linos pantaloneros, géneros etc. <br>
    <b>- Telas livianas :</b> Dacrones, velos, lycras para ropa interior etc. 

  </li>
  <li class="collection-item">
   -2.2.2 - Según su estructura física en:  Telas tejidas y no tejidas <br>

    <b>Telas tejidas:</b> <br>

    <b>-Tejido plano:</b> es el resultado del entrelazamiento de los hilos de urdimbre y los hilos de trama: Dril, dacrón, satín, coleta, etc. <br>
      <b>-Tejido de punto: </b>Es el entrelazamiento de uno o una serie de hilos en si mismo, en forma de bucles que nos dan como resultado un tejido de gran elasticidad. 
    <br> <b>EJEMPLO</b> El rib, el punto sencillo, acanalado etc. <br>
    <b>Telas no tejidas o aglutinados :</b> <br>

    Son aquellas fibras que por medio de un aglutinamiento, una presión y temperatura sometida a fusión ó químico adhesivo forman la tela 
    <br> <b>EJEMPLO:</b> La entretela,telas para pañal desechable, etc.
  </li>


</ul>

