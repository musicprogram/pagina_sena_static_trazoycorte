             2.7.9 - DIFERENCIAS NOTORIAS DE TONO:

             Ya sea orillo – centro – orillo y/o de punta inicial a punta final de cada rollo o pieza. <br>

             2.7.10 - ONDULACIONES O EMBOLSAMIENTOS: engolado ( engolamiento)

             En los orillos o en el centro del tejido que impida su normal desempeño durante los procesos de extendida y corte. <br>

             2.7.11 - DEFECTOS CONTINUOS:

             Cuando un determinado defecto de urdimbre se presenta continuo, se penaliza con cuatro puntos cada metro, hasta una longitud que no exceda de tres metros. Si el defecto sobrepasa los tres metros, la pieza se clasifica en calidad B ó C, según su gravedad (puntos por 100 m2). <br>

             2.7.12 - URDIMBRE DISTINTA:

             Uno o varios hilos de un título mayor o menor al normal de la tela que por lo general afecta longitudes considerables. <br>

             2.7.13 - OCASIONAL:

             Son defectos que se presentan esporádicamente y no se encuentran en una clasificación muy definida. Ejemplo: Goteras. <br>

             2.7.14 - QUIEBRES:

             Son pliegues muy acentuados originados durante diferentes procesos, tanto en tejido como LA tundida y la rama . <br>

             2.7.15 - TRAMO GRUESO URDIMBRE:

             Es una porción de hilo de un grueso superior al normal, puede presentarse desde un centímetro hasta un metro mayor de esta longitud puede codificarse como urdimbre distinta. <br>

             2.7.16 - ORILLO MALO:

             Falta parcial, desflecado, roto o más delgado que el normal <br>

             2.7.17 - MOTAS Y NUDOS:

             Motas: Se presentan en tamaños de 1 a 3 mm y en ocasiones concentradas, son porciones de hilazas excesivamente gruesas.

             Nudos: Son empates de hilos mal hechos <br>

             2.7.18 - PARCHE DE GOMA:

             Exceso de goma en la urdimbre. Se presenta en forma de parche de tacto muy fuerte. <br>

             2.7.19 - BARRADO:

             Sólo se presenta en tela con filamento de poliester o nylon, se presenta tanto en urdimbre como trama. Más común en telas de microfibras <br>

             2.7.20 - HEBRAS SIN CORTAR:

             Extremo de urdimbre o de trama que sobresale de la tela <br>

             2.7.21 - TEJIDO CORRIDO:

             Como su nombre lo indica, es un tejido que se corre, puede tener origen en los tejidos con una construcción muy baja. <br>

             2.7.22 - ACEITE:

             Diferencia de color debido a contaminación del mismo elemento <br>

             2.7.23 - EMPATES:

             Es la costura para unir dos piezas,el cual debe cumplir con las normas establecidas. <br>

             2.7.24 - BORRA:

             Desperdicio de material no hilado, se presenta en forma de tacos, motas en distintos tamaños. <br>

             2.7.25 - CORDON:

             Doble pasada de trama en el mismo ligamento <br>

             2.7.26 - CRESPOS:

             Hilos de trama que se enroscan en sí mismos <br>

             2.7.27 - BASTAS:

             Hilos de urdimbre o trama que quedan flotando <br>
