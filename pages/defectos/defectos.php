  <li class="collection-item">
    -2.7.1 - DEFECTO TEXTIL:

    Es la desviación de una muestra con respecto a una especificación dada. En resumen, los defectos son deficiencias que disminuyen el valor o utilidad de los artículos.</li>
    <li class="collection-item">
      -2.7.2 - ROTOS:

      Se considera como un "roto", cuando dos o más hilos aparecen reventados en un mismo sitio. Todos los rotos se penalizan con cuatro puntos y cuando miden más de un centímetro, este tejido no se incluye en calidad A.
    </li>
    <li class="collection-item">
      -2.7.3 - LLAGAS:

      Son secciones de tejido donde los hilos no están entrelazados. Tienen la misma penalización de los rotos.
    </li>
    <li class="collection-item">
      -2.7.4 - DESPISTES:

      Son ocasionados por la ausencia de hilos de trama, por lo general se dan en todo el ancho de la tela. Si su tamaño, en el sentido de la urdimbre, excede de 0.5 centímetros, tampoco se incluye como calidad A.
    </li>
    <li class="collection-item">
      -2.7.5 - TRAMA DISTINTA:

      Son hilos de trama diferentes, afectando todo el ancho del tejido. Si son mayores de cinco centímetros en el sentido de la urdimbre, no es calidad A.
    </li>
    <li class="collection-item">
      -2.7.6 - DAÑO DE DIBUJO DEL TELAR:

      Deformación del diseño del tejido que no corresponde con las especificaciones. Se presenta en las telas fabricadas en telares de maquinilla o jacquard (tales como dibujos a rayas, cuadros o fantasías), penalizándose igual que la trama distinta.
    </li>
    <li class="collection-item">
      -2.7.7 - PIERNA O ARCO:

      Se presenta cuando los hilos de trama no están completamente perpendiculares a la urdimbre (cuando no están perfectamente a escuadra con los orillos). Desviaciones de la trama mayores al 2% del ancho de la tela, no se admiten en calidad A.
    </li>
    <li class="collection-item">
      -2.7.8 - TELA ANGOSTA:

      No se admiten para calidad A desviaciones mayores del 2% sobre el ancho estipulado como requisito.

