						<b>4.5.1 - Normales :</b> Son similares a las que existen en el mercado y se usan cuando el extendido es de poca altura y corresponde a la cobertura de la boca de la pinza que es de 4 a 8 cms. <br>

						

						<b>4.5.2 - Verticales :</b> Consiste en un fleje horizontal que corre a lo largo de una guía vertical, la marcada y el paquete se introducen entre el fleque y la base de la guía , manteniéndose esta sujeción por presión sobre el fleje horizontal. Con este tipo de pinzas se puede llegar a sujetar alturas de corte de 20 a 25 cms. <br>

						

						El empleo de las pinzas trae consigo el uso de una cantidad considerable de ellas y el cambio reiterado de posición de éstas durante la operación de cortar, ya que estorban para realizar la operación. <br>
