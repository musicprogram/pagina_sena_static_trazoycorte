						En un principio se han empleado las mismas máquinas de cortar para realizar esta operación, pero en muchos casos representa un impedimento para la misma labor de cortar e incluso se aprecian diferencias de profundidad en las marca de unas piezas a otras del mismo colchón, afectando la calidad de la prenda. <br>

						Como elemento auxiliar de esta operación se ha empleado un aparato sencillo que consta de una pequeña cuchilla en forma de uña que está colocada sobre una guía vertical y que se conserva en la posición mas alta debido a un muelle interior. Cuando se desea marcar una referncia exterior en un paquete de telas se aplica el aparato contra ellas y con la mano se presiona hacia abajo de forma que la cuchilla corte todas las hojas o capas <br>



						Esta solución que en un principio parece rápida y económica se convirte en muchos casos difícil e imposible, cuando el número de capa sea elevado , tejidos demaciados rígidos; o tejidos con mucha elasticidad. <br>

						En estos casos se emplean marcadores por calor, que consisten en una cuchilla vertical en forma de V que está conectada a una fuente de calor, producido normalmente por una resistencia eléctrica. Esta tiene varias tormas para producir diferentes intensidades de calor según los tejidos a marcar. <br>
