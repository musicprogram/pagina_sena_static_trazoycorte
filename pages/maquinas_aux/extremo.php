						Uno de los principales problemas de tirar de una tela de un rollo montado al extremo de la mesa de corte, es el tiempo , esfuerzo y tejidos derrochados al cortar cada capa. Puesto que al hacer un corte recto es difícil, el extendedor deja siempre un margen adicional. <br>

						La cortadora de extremos ofrece mejoras significativas en velocidad, eficiencia y precisión en esta operación. La cuchilla circular guiada por un carril permite al extendedor efectuar un corte mas rápido y exacto de cualquier tipo de material. Tiene también un mecanismo de elevación que permite elevar la pista por encima de la capa de corte y luego hacerla bajar sobre el tejido extendido, manteniendo la capa de corte alineada en el extremo de corte. <br>

						Posee también un mecanismo de afilado automático, este se realiza con un esmeril situado en lado izquierdo superior de la cuchilla, este proceso se realiza manualmente. <br>
