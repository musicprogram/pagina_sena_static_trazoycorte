          Dentro de los patrones existen referencias interiores que después han de ser ralizadas con la pinzas troceadas. Tal es el caso de los puntos de clavado de bolsillos de parche, pinzas etc, que deben de quedar colocadas en todas las prendas en la misma posición. <br>

          Para solucionar esta operación se emplea los peforadores. Según su funcionamiento los perforadores son de cuatro tipos: <br>
            <img src="images/sena/pieza2.jpg" class="responsive-img imgs z-depth-1"> <br>
          <b>4.2.1 - PERFORADORES GIRATORIOS</b> <br>

          Estos constan de agujas de distintos diámetros según el tipo de tejidos a taladrar . Está conectada a un motor que la hacen girar y todo el conjunto está sujeto a un armazón que lo mantiene en posición vertical . Este tipo de perforador se coloca sobre el paquete de telas encima de la marca que hay que taladrar y haciendo bajar el conjunto de motor y aguja, esta va penetrando en el tejido , realizando un agujero en todas las telas y en el punto deseado. <br>

          En los tejidos sintéticos se acondiciona en la base del perforador un bloque calefactor que calienta la aguja , esta al entrar en el colchón de tela va quemando el borde del agujero fundiéndose al retirar la aguja o taladro. Al fundirse el tejido da una mayor estabilidad y por lo tanto una mayor calidad en las piezas cortadas. <br>

          <b>4.2.2 - PERFORADORES ALTERNATIVOS</b>
 <br>
          Este tipo de prforador transforma por medio de un juego de engranajes reductores el movimiento giratorio del motor en alternativo; al mismo tiempo que ejecuta un vaivén de percusión, con todo esto se consigue que la aguja abra la malla del tejido sin romperla , consiguiendo un agujero limpio y sin estropear la pieza que se está taladrando. <br>

          <b>4.2.3- PERFORADORES DE TINTA</b> <br>

          Consta de una aguja taladrada en su interior, por donde se introduce una aguja fluorecente que está conectada a un motor de bajas revoluciones , de esta forma la aguja penetra en el tejido fácilmente gracias a su movimiento giratorio y va marcando un punto entintado en cada capa. Este punto únicamente será visible cuando la prenda se encuentra bajo una luz ultravioleta , por lo tanto dentro del equipo stándar de estos perforadores se incluye una lámpara de tubos infrarojos para colocar en el puesto de trabajo, dentro de la sala de costura , donde se haga necesario ver los puntos y marcas para realizar la operación correspondiente. <br>



          El operario al realizar la operación debe cuidar que la aguja o taladro no perfore la superficie de la mesa , para evitar esto se utiliza una hoja metálica o de madera delgada que se colocará entre la superficie de la mesa y el tendido.
