					<div class="cyan-text text-darken-2">

						Una vez realizado el corte se efectúa en la mayoría de los casos el etiqueteado de todas las piezas que componen cada uno de los paquetes . Estas etiquetas tienen la misión de servir de control y deben llevar las referncias suficientes para identificar cualquier pieza y determinar a que paquete pertenecen y con que pieza debe unirse. <br>

						Para ello, existen diferentes numeradores en el mercado trabajando con números o imprentillas. La información que debe llevar es la siguiente: Talla, modelo, orden consecutivo de la capas. Estas últimas cifras saltarán de una en una cuando se extienda cara arriba y de dos en dos cuando se extienda en zig - zag. <br>

						Existen dos tipos de máquinas a emplear: <br>
						
						<b>4.4.1 - MÁQUINAS MÓVILES:</b>

						<br>
						Son portátiles conocidas como pistolas de etiquetar, muy similares en su funcionamiento a las empleadas en grandes almacenes y suprmercados para marcar los diferentes productos. <br>

						<b> 4.4.2 - MÁQUINAS FIJAS :</b> <br>




						Son las que permanecen en un lugar determinado y necesitan que los paquetes a etiquetar vayan hasta ella. Estas máquinas según la forma de fijar la etiqueta se dividen en : <br>

						

						<b>Tiqueteadoras Termoadhesivas :</b> llevan un cabezal calefactor que puede graduarse según el tejido a etiquetar y las etiquetas llevan un compuesto termo - adherente, con lo cual al ponerse en contacto la tela con la fuente de calor , queda fijada. Todo el ciclo de movimiento, etiquetado y pegado se realiza en forma automática. <br>
						<b>Tiqueteadora de doble cabeza :</b>Cuando el extendido se realiza en zig - zag se puede emplear la etiqueteadora de doble cabeza, con la cual se consigue colocar dos etiquetas al mismo tiempo, reduciendo los tiempos de trabajo considerablemente. <br>
						<b>Tiqueteadora con fijación de cocido:</b> son muy parecidas a las antriores, con la diferencia de que a la hora de fijar la etiqueta al tejido la máquina da tres puntadas de un hilo. Estas máquinas estan diseñadas para trabajar con tejidos difíciles donde la etiqueta termoadhesiva o adhesiva puede dejar mancha o pegar mal , cuando se trabaja con una gama muy amplia de tejidos.
						
