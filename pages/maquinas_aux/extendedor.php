               <b>Extendido con tensión:</b> <br>
          El desarrollo de la pieza de tejido se consigue por la tensión que origina el carro al desplazarse. En general es para tejidos de calada ( urdimbre y trama). Esta tensión no origina grandes problemas al extenderlo. <br>
           <img src="images/sena/pieza1.jpg" class="responsive-img imgs z-depth-1">
 <br>
          <b> Extendido sin tensión ( positivo ) : </b> <br> 
          Los tejidos de género de punto deben ser extendidos sin tensión. Para este caso el carro dispone de unos mecanismos regulables de desarrollo de la pieza de tejido y otros de elimentación al desprenderse éste sobre lña mesa de tendido. Igualmente puede estirarse el género de punto tubular mediante un dispositivo ensanchador situado en el mecanismo de desprendimiento. El desplazamiento del carro se realiza automáticamente haciendo el recorrido de vaivén de acuerdo con la longitud por extender. La sujeción de los extremos del tendido es igualmente automático en los tendidos con extensión. Para el caso de los tejidos de punto con desarrollo positivo ( sin tensión) no es necesaria esta sujeción. <br>

          El empleo de carros extendedores y dentro de estos la elección de unos o otros dependerá de las características en las de fabricación que pueden ser: largo del tendido , número de capas a extender y/o tipo de tejido. <br>

          En general puede decirse que el grado de interés es creciente en función directa al número de telas por extender de la misma pieza. 
