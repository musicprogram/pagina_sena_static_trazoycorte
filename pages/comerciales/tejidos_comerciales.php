

<ul class="collection with-header">
  <li class="collection-header cyan-text text-darken-2 right-align"><h5>TEJIDOS MAS COMERCIALES <small>Tejidos planos</small> </h5></li>


<li class="collection-item">
      -2.4.1.1 - GABARDINAS:

      Telas de tejido diagonal 2 X 1 derecho o izquierdo, de hilos de algodón 100% o mezcla con poliester, de peso más liviano que los driles.

      También se le da este nombre a telas con acabado impermeable para la confección de chaquetas y abrigos.




    </li>

    <li class="collection-item">
      -2.4.1.2 - DRILES:

      Telas de tejido diagonal 3 X 1 derecho o izquierdo, de hilos de algodón 100% o mezclado con poliester, más pesados que la gabardina, son apropiados para la confección de pantalonería.
    </li>
    <li class="collection-item">
      -2.4.1.3 -GENEROS:

      Telas de tejido tafetán de hilos de algodón 100% o mezclado con poliester, de peso liviano (110 a 140 GR/MT2) y adecuados para la confección de sábanas, sobresábanas y manteles, ropa deportiva e interior
    </li>
    <li class="collection-item">
      -2.4.1.4 - FAYAS:

      Telas de tejido tafetán, donde los hilos de la urdimbre son mucho más delgados que los hilos de la trama y presentan surcos a lo ancho de la tela.

      Se pueden tejer de poliester, nylon, rayon viscosa, algodón o mezclas de estas fibras.

      Estas telas generalmente son muy dilicadas, por ello no son apropiadas para la confección de uniformes y las prendas deben ser amplias y llevar costura de seguridad, para evitar que se deslicen los hilos en las zonas de las costuras al aplicarle tensión.

    </li>
    <li class="collection-item">
      -2.4.1.5 - LINO:

      Son elaboradas de fibras obtenidas de los tallos de algunas plantas herbáceas, de la familia de las lináceas, las cuales son cultivadas en Francia, India y Brasil.

      Generalmente estas telas presentan motas de diferentes tamaños, repartidas en el tejido de forma irregular.

      También se le denomina linos a algunas telas elaboradas con poliester en mezcla con algodón o rayón viscosa, las cuales tienen apariencia del lino natural por la irregularidad de los hilos que la conforman.
    </li>
    <li class="collection-item">
      -2.4.1.6 - POPELINAS:

      Telas de tejidos tafetán, de hilos de algondón 100% o mezclado con poliester, de pesos que oscilan entre 80 y 150 gramos por metro cuadrado.

      Las de peso entre 80 y 120 gramos son apropiadas para la confección de camisas, blusas y vestuario infantil.

      Las que tienen peso entre 120 y 150 gramos son destinadas a la confección de pantalonería liviana, slaks y vestuario femenino.
    </li>
    <li class="collection-item">
      -2.4.1.7 - OPALES:

      Telas de tejido tafetán, de hilos de algodón 100% o mezclado con poliester, con títulos delgados de 40/1 ó 50/1, son apropiadas para la confección de camisas, blusas, pañuelos y pañoletas.
    </li>
    <li class="collection-item">
      -2.4.1.8 - CHAMBRAY:

      Telas de tejido tafetán, de hilos de algodón 100% o mezclado con poliester, los hilos de la urdimbre son preteñidos y la trama blanca o cruda. Son apropiados para la confección de pantalonería liviana, camisería y vestuario femenino.
    </li>
    <li class="collection-item">
      -2.4.1.9 - CHALLIS:

      Telas tejidas con fibras de rayón viscosa y en mezclas con poliester. Se distinguen por presentar tacto suave y buena caída.

      Estas prendas requieren de cuidados especiales al lavado, secado y planchado, por ello no son apropiadas para la confección de uniformes.
    </li>
    <li class="collection-item">
      -2.4.1.10 - PRETEÑIDOS:

      En estas telas los hilos, en forma parcial o total, son teñidos antes de tejer la tela.
    </li>
    <li class="collection-item">
      -2.4.2 - TEJIDOS DE PUNTO: Este tema falta, después se completa
    </li>





</ul>

























