  <li class="collection-header cyan-text text-darken-2 right-align"><h5>ACABADOS TEXTILES</h5></li>
  


    <li class="collection-item">-2.5.1 - WASTER REPELENT:

      Operación de acabados que transforma los tejidos en telas impermeables al agua, bien por recubrimiento de superficie por una película o capa, bien por transformación del mismo en hidrófobo, mediante tratamiento con una sustancia hidrofónica.
    </li>
    <li class="collection-item">-2.5.2 - ESMERILADO:

      Operación que recibe el tejido al ser sometido a abrasión son cilindros metálicos forrados con papel esmeril, para obtener tactos en la superficie de la tela similares a la gamuza.
    </li>
    <li class="collection-item">-2.5.3 - FELPADO:

      Operación de abrasión que recibe el tejido al ser sometido a fricción con cilindros cubiertos de puas metálicas que giran en forma alternada en un tambor, dotado también de un movimiento diferente a los cilindros para obtener sobre la superficie del tejido un tacto similar o igual a la tela dulce abrigo.
    </li>
    <li class="collection-item">-2.5.4 - PREENCOGIDO:

      Fijar por medio de comprensión las telas de algodón, dependiendo de análisis previos de lavado a la tela a procesar, se estira y se comprime una banda de hule, luego se coloca la tela entre ella y unos cilindros metálicos. La banda se contrae a su posición normal y también la tela, produciendo el encogimiento programado.

      En los procesos siguientes se debe evitar cualquier tipo de tensión sobre la tela.
    </li>
    <li class="collection-item">-2.5.5 - DEVORE:

      Aplicación por estampación de una pasta preparada con productos químicos los cuales reaccionan al recibir altas temperaturas, degradando totalmente las fibras celulósicas (algodón) que serán retiradas posteriormente por medio de lavados.

      Este proceso es aplicado a telas de poliester mezclado con algodón, quedando en las zonas afectadas solamente las fibras de poliester.
    </li>
    <li class="collection-item">-2.5.6 - FELPA CORTADA:

      Proceso aplicado a determinadas toallas para darles un aspecto agradable. Consiste en cortar el loop que forman los hilos por una sola cara de la tela.
    </li>

    <li class="collection-item">
      -2.5.7 - CHINTZ:

      Operación de fricción que recibe el tejido de algodón, ( previa aplicación de ceras y resinas), por medio de cilindros metálicos y de papel o algodón que giran a diferente velocidad, lo que le confiere al tejido cierta rigidez, un tacto liso y frío y en especial una superficie brillante.
    </li>
    <li class="collection-item">
      - 2.5.8 - VELADO:

      Este proceso generalmente se aplica a telas livianas que tengan mezcla íntima de fibras de poliester y algodón.

      La finalidad es destruir las fibras de algodón dejando intactas las de poliester, resultando de ello una tela tipo velo.

      La tela es procesada en una máquina termofijadora :

      Impregnación de productos químicos en el foulard.

      Secado

      Exposición del tejido a altas temperaturas, donde los productos utilizados generan determinados ácidos, los cuales carbonizar el algodón.

      Lavado para eliminar residuos de fibras destruidas
    </li>


</ul>













