  <li class="collection-item">


    Los rollos de tela requieren cuidados especiales tanto del transportador como el confeccionista. Con la siguiente sugerencia, tratamos de que dichos cuidados sean cumplidos, ya que nada logramos si la tela que es acabada con grandes cuidados es tratada en forma no adecuada.

    Las siguientes son algunas recomendaciones para almacenaje y movimiento de tela: <br>

    Los rollos deben ser colocados en el carro transportador en forma horizontal y todos en la misma dirección. Por ningún motivo se debe cruzar, ya que al hacerlo, los rollos inferiores deben resistir un peso muy alto repartidos en áreas muy pequeñas, originando presiones altísimas. Esta presión deforma el rollo que luego se refleja en el tendido formando ondulaciones.
    <br>


    En el momento del desargue hay que tener cuidado al colocarlos sobre el piso o lugar de almacenamiento. Un golpe en los extremos produce un engolado de orillos, entonces es necesario tener cuidado al colocarlo, no dejándolo caer. El engolado de orillos dificulta notoriamente el tendido, el corte y demás obliga y limita hacer tendido de menos número de capas y también dificulta el tener un buen tallaje. El engolado es tan considerable que afecta el tamaño de piezas o parte de prendas dando luego prendas imperfectas o difíciles de ensamblar.

    <br>

    El confeccionista debe observar cuidadosamente la bajada de los rollos del carro transportador y si es necesario hacer los comentarios y observaciones correspondientes. <br>

    Los diferentes sistemas de transporte se realizan cuando existen limitantes físicas, poco espacio y mesas muy pequeñas para realizar las operaciones en el departamento de corte. <br>

    Mientras se disponga de espacios amplios y masas lo suficientemente largas se recomienda no mover ni desplazar para nada los tendidos ó sea realizar todas las operaciones en el mismo sitio donde se hizo el extendido



