    -2.9.1 - SISTEMAS DE TRANSPORTE

    

    <div class="card-content cyan-text darken-3">

      -2.9.1.1 – TRANSPORTE MANUAL <br>

      Método más sencillo en el que varios operarios sujetan el extendido y tiran de él para desplazarlo a un lugar ya determinado. Puede presentar deformaciones internas en el tendido debido a que la fuerza que se le imprime es diferente, para evitar esto se coloca una hoja de papel kraft con el objetivo de que el colchón se deslice fácilmente sobre la mesa.

      Es importante tener en cuenta que la cara lisa de papel quede en contacto con la mesa y la cara áspera quede en contacto con las telas.

      -2.9.1.2 – TRANSPORTE POR AEROSUSPENSIÓN <br>

      Surgen los dispositivos de aire soplado acoplados a las mesas de corte. En síntesis consiste en hacer fluir aire a cierta velocidad por unos orificios efectuados en el tablero de la mesa de corte. Esto hace que el bloque o colchón de tela se deslice facilmente sobre la mesa, evitando así las deformaciones internas en el tendido, y por lo tanto el operario hará un menor esfuerzo.
      <br>
      -2.9.1.3 – TRANSPORTE POR AEROSUSPENSIÓN Y RODILLOS DE AVANCE <br>

      Es el mismo sistema de aerosuspensión pero se le acondicionan unos rodillos que giran constantemente. Esos rodillos están colocados longitudinalmente a la mesa, cuyo objetivo es desplazar el tendido o colchón automáticamente. Hay que cuidar que uno de los extremos del tendido esté en contacto con estos rodillos. Un solo operario es necesario para mover el colchón.

      <br>

      -2.9.1.4 – TRANSPRTE POR CINTAS O BANDAS <br>

      Consiste en el empleo de cintas o bandas movidas por un sistema de transmisión accionado por un motor eléctrico. Con este sistema se consiguen varios objetivos:



      Transporte automático sin la ayuda de un operario

      Un transporte que no afecta el colchón ni le produce deformaciones internas

      Se mantiene a los operarios en sus puestos de trabajo sin péredidas de tiempo ni movimientos improductivos.


      <br>

      -2.9.1.5 – TRANSPORTE POR MESA DE TRANSFERENCIA <br>

      En este método se traslada la máquina extendedora no el extendido. Todas las operaciones se realizan en el mismo sitio de trabajo (extendido, corte y empaquetado).

      Este transporte está unido a todo un proceso sistematizado, el cual nos permite mayor rapidez y mayor calidad en el departamento de corte.

