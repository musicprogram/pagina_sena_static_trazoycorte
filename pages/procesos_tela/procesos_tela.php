  <li class="collection-header cyan-text text-darken-2 right-align"><h5>PROCESOS EN LAS TELAS PARA DAR ESTABILIDAD DIMENSIONAL AL LAVADO</h5></li>

  

  <li class="collection-item">
      -2.6.1 - TERMOFIJADO:

      Fijar por medio de temperatura las telas que tienen en su composición fibras de: poliester, nylon, acrílico y dacrones modificados. Variables :

      Temperatura:  De 160 a 210 C

      Tiempo de exposición: De 30 a 80 segundos

      Máquinas: Termofijadora, estiradora o rama

    </li>
    <li class="collection-item">
      -2.6.2 - RESINAS:

      Fijar por medio de productos químicos las telas que tengan en su composición fibras celulósicas (algodón, viscosa, lino).

      Aplicación de resinas en foulard de impregnación de baños que contienen además suavizantes y otros productos para el acabado final de las telas.

      Luego de secar, la tela se somete a determinada temperatura y tiempo de exposición adecuados para la reacción química de la resina.
    </li>
    
</ul>

